public class BannerImageHolder implements Holder<BannerEntity> {

    private AppCompatImageView mImageView = null;
    private static final RequestOptions BANNER_OPTIONS = new RequestOptions()
            .diskCacheStrategy(DiskCacheStrategy.ALL)
            .dontAnimate()
            .centerCrop();

    @Override
    public View createView(Context context) {
        mImageView = new AppCompatImageView(context);
        return mImageView;
    }

    @Override
    public void UpdateUI(Context context, int position, BannerEntity data) {
        Glide.with(context)
                .load(data.getPic_url())
                .apply(BANNER_OPTIONS)
                .into(mImageView);
    }
}
